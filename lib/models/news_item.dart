class NewsItem {
  final String title;
  final String subtitle;
  final String author;
  final String authorRole;
  final String timeAgo;
  final String views;
  final int likes;
  final int comments;
  final int shares;
  final String image;

  NewsItem({
    required this.title,
    required this.subtitle,
    required this.author,
    required this.authorRole,
    required this.timeAgo,
    required this.views,
    required this.likes,
    required this.comments,
    required this.shares,
    required this.image,
  });
}
